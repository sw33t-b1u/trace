"""Tests for validate/semantic/identity_assets.py + the CLI wrapper."""

from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

import pytest
from pydantic import ValidationError

from trace_engine.validate.schema import (
    Asset,
    AssetsDocument,
    HasAccessEntry,
    IdentityAssetsDocument,
    IdentityEntry,
)
from trace_engine.validate.semantic.identity_assets import check_identity_assets

REPO = Path(__file__).parent.parent
CMD = REPO / "cmd" / "validate_identity_assets.py"


# ---------------------------------------------------------------------------
# Pydantic schema
# ---------------------------------------------------------------------------


class TestSchema:
    def test_minimal_identity(self):
        ident = IdentityEntry(id="id-x", name="X")
        assert ident.identity_class == "group"
        assert ident.sectors == []

    def test_identity_class_validation_rejects_unknown(self):
        with pytest.raises(ValidationError):
            IdentityEntry(id="id-x", name="X", identity_class="bogus")

    def test_access_level_default_is_read(self):
        edge = HasAccessEntry(identity_id="id-x", asset_id="asset-1")
        assert edge.access_level == "read"

    def test_granted_revoked_inversion_rejected(self):
        with pytest.raises(ValidationError, match="must precede"):
            HasAccessEntry(
                identity_id="id-x",
                asset_id="asset-1",
                granted_at="2024-12-01",
                revoked_at="2024-01-01",
            )

    def test_high_value_impersonation_flag_defaults_false(self):
        """Initiative C Phase 2 (BEACON 0.13.0 / TRACE 1.6.0): the new flag
        defaults to False so BEACON 0.12.x identity_assets without it
        remain valid."""
        ident = IdentityEntry(id="id-x", name="X")
        assert ident.is_high_value_impersonation_target is False
        assert ident.impersonation_risk_factors == []

    def test_high_value_impersonation_flag_roundtrips(self):
        ident = IdentityEntry(
            id="id-cfo",
            name="Sample Victim CFO",
            identity_class="individual",
            roles=["cfo"],
            is_high_value_impersonation_target=True,
            impersonation_risk_factors=["executive", "public-facing-brand"],
        )
        assert ident.is_high_value_impersonation_target is True
        assert ident.impersonation_risk_factors == ["executive", "public-facing-brand"]

    def test_document_accepts_optional_comment_field(self):
        # BEACON's generator emits a `_comment` hint at the top level;
        # IdentityAssetsDocument uses `extra='ignore'` to accept it.
        doc = IdentityAssetsDocument.model_validate(
            {
                "version": 1,
                "_comment": "Generated by BEACON ...",
                "identities": [],
                "has_access": [],
            }
        )
        assert doc.version == 1


