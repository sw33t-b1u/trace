def _assets_doc(asset_ids: list[str]) -> AssetsDocument:
    return AssetsDocument(
        assets=[Asset(id=aid, name=aid) for aid in asset_ids],
    )


class TestCrossReference:
    def test_clean_doc_yields_no_findings(self):
        ia = IdentityAssetsDocument(
            identities=[IdentityEntry(id="id-x", name="X")],
            has_access=[HasAccessEntry(identity_id="id-x", asset_id="asset-1")],
        )
        a = _assets_doc(["asset-1"])
        findings = check_identity_assets(ia, a)
        assert findings == []

    def test_dangling_identity_id_is_error(self):
        ia = IdentityAssetsDocument(
            identities=[],
            has_access=[HasAccessEntry(identity_id="id-ghost", asset_id="asset-1")],
        )
        a = _assets_doc(["asset-1"])
        findings = check_identity_assets(ia, a)
        codes = [f.code for f in findings]
        assert "HAS_ACCESS_REF_IDENTITY" in codes

    def test_dangling_asset_id_is_error(self):
        ia = IdentityAssetsDocument(
            identities=[IdentityEntry(id="id-x", name="X")],
            has_access=[HasAccessEntry(identity_id="id-x", asset_id="asset-ghost")],
        )
        a = _assets_doc(["asset-1"])
        findings = check_identity_assets(ia, a)
        codes = [f.code for f in findings]
        assert "HAS_ACCESS_REF_ASSET" in codes

    def test_duplicate_identity_id_is_error(self):
        ia = IdentityAssetsDocument(
            identities=[
                IdentityEntry(id="id-x", name="X"),
                IdentityEntry(id="id-x", name="X (dup)"),
            ],
            has_access=[],
        )
        a = _assets_doc([])
        findings = check_identity_assets(ia, a)
        assert any(f.code == "IDENTITY_ID_NOT_UNIQUE" for f in findings)

    def test_duplicate_access_pair_is_warning(self):
        ia = IdentityAssetsDocument(
            identities=[IdentityEntry(id="id-x", name="X")],
            has_access=[
                HasAccessEntry(identity_id="id-x", asset_id="asset-1"),
                HasAccessEntry(
                    identity_id="id-x",
                    asset_id="asset-1",
                    access_level="admin",
                ),
            ],
        )
        a = _assets_doc(["asset-1"])
        findings = check_identity_assets(ia, a)
        warns = [f for f in findings if f.severity == "warning"]
        assert any(f.code == "HAS_ACCESS_DUPLICATE_PAIR" for f in warns)


