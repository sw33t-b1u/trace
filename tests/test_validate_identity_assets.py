def _write(path: Path, payload: dict) -> Path:
    path.write_text(json.dumps(payload), encoding="utf-8")
    return path


def _clean_files(tmp_path: Path) -> tuple[Path, Path]:
    ia = _write(
        tmp_path / "identity_assets.json",
        {
            "version": 1,
            "identities": [{"id": "id-x", "name": "X"}],
            "has_access": [{"identity_id": "id-x", "asset_id": "asset-1"}],
        },
    )
    a = _write(
        tmp_path / "assets.json",
        {
            "network_segments": [],
            "security_controls": [],
            "assets": [{"id": "asset-1", "name": "Asset 1"}],
            "asset_connections": [],
            "asset_vulnerabilities": [],
            "actor_targets": [],
        },
    )
    return ia, a


class TestCli:
    def test_exit_zero_on_clean(self, tmp_path: Path):
        ia, a = _clean_files(tmp_path)
        result = subprocess.run(
            [sys.executable, str(CMD), "--identity-assets", str(ia), "--assets", str(a)],
            capture_output=True,
            text=True,
            check=False,
        )
        assert result.returncode == 0, result.stderr
        assert "errors=0" in result.stdout

    def test_exit_one_on_dangling_asset(self, tmp_path: Path):
        ia = _write(
            tmp_path / "identity_assets.json",
            {
                "version": 1,
                "identities": [{"id": "id-x", "name": "X"}],
                "has_access": [{"identity_id": "id-x", "asset_id": "asset-ghost"}],
            },
        )
        a = _write(
            tmp_path / "assets.json",
            {
                "network_segments": [],
                "security_controls": [],
                "assets": [{"id": "asset-1", "name": "Asset 1"}],
                "asset_connections": [],
                "asset_vulnerabilities": [],
                "actor_targets": [],
            },
        )
        result = subprocess.run(
            [sys.executable, str(CMD), "--identity-assets", str(ia), "--assets", str(a)],
            capture_output=True,
            text=True,
            check=False,
        )
        assert result.returncode == 1
        assert "errors=" in result.stdout
