"""Tests for validate/semantic/user_accounts.py + the CLI wrapper."""

from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

import pytest
from pydantic import ValidationError

from trace_engine.validate.schema import (
    AccountOnAssetEntry,
    Asset,
    AssetsDocument,
    IdentityAssetsDocument,
    IdentityEntry,
    UserAccountEntry,
    UserAccountsDocument,
)
from trace_engine.validate.semantic.user_accounts import check_user_accounts

REPO = Path(__file__).parent.parent
CMD = REPO / "cmd" / "validate_user_accounts.py"


def _assets_doc(asset_ids: list[str]) -> AssetsDocument:
    return AssetsDocument(assets=[Asset(id=aid, name=aid) for aid in asset_ids])


def _identity_assets_doc(identity_ids: list[str]) -> IdentityAssetsDocument:
    return IdentityAssetsDocument(
        identities=[IdentityEntry(id=ident, name=ident) for ident in identity_ids],
    )


