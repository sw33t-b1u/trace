def _write(path: Path, payload: dict) -> Path:
    path.write_text(json.dumps(payload), encoding="utf-8")
    return path


def _clean_files(tmp_path: Path) -> tuple[Path, Path]:
    ua = _write(
        tmp_path / "user_accounts.json",
        {
            "version": 1,
            "user_accounts": [{"id": "ua-x", "account_login": "x"}],
            "account_on_asset": [{"user_account_id": "ua-x", "asset_id": "asset-1"}],
        },
    )
    a = _write(
        tmp_path / "assets.json",
        {
            "network_segments": [],
            "security_controls": [],
            "assets": [{"id": "asset-1", "name": "A"}],
            "asset_connections": [],
            "asset_vulnerabilities": [],
            "actor_targets": [],
        },
    )
    return ua, a


class TestCli:
    def test_exit_zero_on_clean(self, tmp_path: Path):
        ua, a = _clean_files(tmp_path)
        result = subprocess.run(
            [sys.executable, str(CMD), "--user-accounts", str(ua), "--assets", str(a)],
            capture_output=True,
            text=True,
            check=False,
        )
        assert result.returncode == 0, result.stderr
        assert "errors=0" in result.stdout

    def test_exit_one_on_dangling_asset(self, tmp_path: Path):
        ua = _write(
            tmp_path / "user_accounts.json",
            {
                "version": 1,
                "user_accounts": [{"id": "ua-x", "account_login": "x"}],
                "account_on_asset": [{"user_account_id": "ua-x", "asset_id": "asset-ghost"}],
            },
        )
        a = _write(
            tmp_path / "assets.json",
            {
                "network_segments": [],
                "security_controls": [],
                "assets": [{"id": "asset-1", "name": "A"}],
                "asset_connections": [],
                "asset_vulnerabilities": [],
                "actor_targets": [],
            },
        )
        result = subprocess.run(
            [sys.executable, str(CMD), "--user-accounts", str(ua), "--assets", str(a)],
            capture_output=True,
            text=True,
            check=False,
        )
        assert result.returncode == 1
        assert "errors=" in result.stdout
