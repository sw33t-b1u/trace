class TestSchema:
    def test_minimal_user_account(self):
        ua = UserAccountEntry(id="ua-x", account_login="x")
        assert ua.account_type == "other"
        assert ua.is_privileged is False
        assert ua.is_service_account is False

    def test_account_type_validation_rejects_unknown(self):
        with pytest.raises(ValidationError):
            UserAccountEntry(id="ua-x", account_login="x", account_type="bogus")

    def test_first_seen_after_last_seen_rejected(self):
        with pytest.raises(ValidationError, match="must precede"):
            AccountOnAssetEntry(
                user_account_id="ua-x",
                asset_id="asset-1",
                first_seen="2024-12-01",
                last_seen="2024-01-01",
            )

    def test_document_accepts_optional_comment_field(self):
        doc = UserAccountsDocument.model_validate(
            {
                "version": 1,
                "_comment": "Generated by BEACON ...",
                "user_accounts": [],
                "account_on_asset": [],
            }
        )
        assert doc.version == 1


