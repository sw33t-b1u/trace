class TestCrossReference:
    def test_clean_doc_yields_no_findings(self):
        doc = UserAccountsDocument(
            user_accounts=[UserAccountEntry(id="ua-x", account_login="x")],
            account_on_asset=[AccountOnAssetEntry(user_account_id="ua-x", asset_id="asset-1")],
        )
        a = _assets_doc(["asset-1"])
        assert check_user_accounts(doc, a) == []

    def test_dangling_user_account_id_is_error(self):
        doc = UserAccountsDocument(
            user_accounts=[],
            account_on_asset=[AccountOnAssetEntry(user_account_id="ua-ghost", asset_id="asset-1")],
        )
        a = _assets_doc(["asset-1"])
        codes = [f.code for f in check_user_accounts(doc, a)]
        assert "ACCOUNT_ON_ASSET_REF_USER_ACCOUNT" in codes

    def test_dangling_asset_id_is_error(self):
        doc = UserAccountsDocument(
            user_accounts=[UserAccountEntry(id="ua-x", account_login="x")],
            account_on_asset=[AccountOnAssetEntry(user_account_id="ua-x", asset_id="asset-ghost")],
        )
        a = _assets_doc(["asset-1"])
        codes = [f.code for f in check_user_accounts(doc, a)]
        assert "ACCOUNT_ON_ASSET_REF_ASSET" in codes

    def test_duplicate_user_account_id_is_error(self):
        doc = UserAccountsDocument(
            user_accounts=[
                UserAccountEntry(id="ua-x", account_login="x"),
                UserAccountEntry(id="ua-x", account_login="x2"),
            ],
            account_on_asset=[],
        )
        codes = [f.code for f in check_user_accounts(doc, _assets_doc([]))]
        assert "USER_ACCOUNT_ID_NOT_UNIQUE" in codes

    def test_duplicate_account_asset_pair_is_warning(self):
        doc = UserAccountsDocument(
            user_accounts=[UserAccountEntry(id="ua-x", account_login="x")],
            account_on_asset=[
                AccountOnAssetEntry(user_account_id="ua-x", asset_id="asset-1"),
                AccountOnAssetEntry(user_account_id="ua-x", asset_id="asset-1"),
            ],
        )
        all_findings = check_user_accounts(doc, _assets_doc(["asset-1"]))
        warns = [f for f in all_findings if f.severity == "warning"]
        assert any(f.code == "ACCOUNT_ON_ASSET_DUPLICATE_PAIR" for f in warns)


class TestIdentityCrossReference:
    def test_unknown_identity_id_when_doc_supplied_is_error(self):
        doc = UserAccountsDocument(
            user_accounts=[UserAccountEntry(id="ua-x", account_login="x", identity_id="id-ghost")],
            account_on_asset=[],
        )
        codes = [
            f.code
            for f in check_user_accounts(doc, _assets_doc([]), _identity_assets_doc(["id-other"]))
        ]
        assert "USER_ACCOUNT_REF_IDENTITY" in codes

    def test_unknown_identity_id_silent_when_no_identity_doc(self):
        # Without --identity-assets we don't know the identity universe,
        # so this is intentionally not an error.
        doc = UserAccountsDocument(
            user_accounts=[UserAccountEntry(id="ua-x", account_login="x", identity_id="id-ghost")],
            account_on_asset=[],
        )
        codes = [f.code for f in check_user_accounts(doc, _assets_doc([]))]
        assert "USER_ACCOUNT_REF_IDENTITY" not in codes


